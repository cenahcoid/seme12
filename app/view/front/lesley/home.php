    <h1 class="text-center"><b>Kisah Lesley Mobile Legends, Penembak Jitu Pelindung Para Bangsawan</b></h1>
    <br />
        <p style="font-size:20px;">Hero Lesley sendiri lebih identik di tier-tier menengah seperti Epic atau Legend. Bahkan tak jarang para pemain menganggap orang-orang yang memilih Lesley adalah sosok yang siap nge-troll di dalam game.</p>
        <p style="font-size:20px;">Hero Marskman ini dianggap sebagai hero yang memiliki damage yang besar apabila sudah memasuki late game. Karena memang, Lesley adalah hero spesialis late game. Karena itu banyak yang menggunakan Lesley.</p>
        <p style="font-size:20px;">Dalam kisahnya, Lesley sendiri adalah keturunan bangsawan. Ia menjadi guru sekaligus pelindung dari satu-satunya pewaris yang bernama Harley. Dengan senapan panjang milik mendiang ayahnya, Lesley bertransformasi menjadi seorang penembak jitu. Sampai-sampai dia menutup satu matanya, agar dapat membidik musuhnya dengan baik.</p>
        <p style="font-size:20px;">Nah, ingin tahu kisah lengkapnya? Berikut Dafunda Game sudah rangkum informasi mengenai kisah Lesley dalam game Mobile Legends. Daripada berlama-lama, langsung saja simak informasinya berikut ini.</p>
        <p style="font-size:20px;">Sebelum membagikan kisah dari hero ini, ada baiknya kalian kenali dulu setiap skill yang terdapat pada Lesley. Hero Mobile Legends yang satu ini memiliki skill mematikan yang tentunya sangat mengganggu ketika saat war.</p>
        <p style="font-size:20px;">Untuk menguasai hero baru yang satu ini, kalian tentunya harus mengetahui dan mempelajari setiap skill yang terdapat pada hero ini. Berikut skill-skill Lesley yang wajib kalian kuasai:</p>
        <h1><b>Lethal Shoot (Skill Pasif)</b></h1>
        <p style="font-size:20px;">Lesley memulihkan 5 Energy setiap kali ia mengenai lawan dengan Basic Attack. Jika dia tidak menerima Damage selama 5 detik, Basic Attack berikutnya akan memperoleh jarak serangan tambahan dan 50% Critical Chance serta memberikan 1.2 kali Damage. Basic Attack yang diperkuat juga akan memulihkan Energy sebanyak dua kali lipat. (Lethal Shot) memberikan 20% Damage tambahan kepada Minion.</p>
        <p style="font-size:20px;">Cooldown dari (Lethal Shot) akan ter-Reset setiap kali Lesley menggunakan Skill. Setiap jumlah tetap Physical Penetration yang Lesley peroleh akan dikonversikan menjadi 1% Critical Chance (persentase Physical Penetration tidak terpengaruh).</p>
        <h1><b>Master of Camouflage (Skill 1)</b></h1>
        <p style="font-size:20px;">Lesley memasuki Mode Camouflage dan memperoleh pemulihan Energy dua kali lipat, 40% Movement Speed tambahan, serta 75 Physical Attack selama 3 detik. Memberikan atau menerima Damage akan mengakhiri Mode ini. Lawan dapat mendeteksi kamuflase Lesley melalui distorsi dari lingkungan di sekeliling mereka.</p>
        <h1><b>Tactical Grenade (Skill 2)</b></h1>
        <p style="font-size:20px;">Lesley melemparkan Tactical Grenade ke depan, yang meledak dalam area berbentuk kipas di depannya. Skill ini memberikan 150( +50% Total Physical ATK) (Physical Damage) kepada lawan dan menyebabkan efek Knockback kepada mereka. Sementara itu Lesley sedikit melompat ke belakang. Menggunakan Skill ini akan secara langsung membatalkan (Ultimate Snipe) dan mengeluarkan Fatal Bullet.</p>

